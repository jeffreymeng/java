package arraysWorksheet1;

import java.util.Scanner;

public class UserInputArray {

	public static void main(String[] args) {
		Scanner in = new Scanner(System.in);
		int[] age = new int[20];
		for (int i = 0; i < age.length; i ++) {
			System.out.println("Enter age #" + i + ":");
			age[i] = in.nextInt();
		}

	}

}
